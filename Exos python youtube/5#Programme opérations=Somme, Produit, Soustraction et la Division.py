def Opérations(val1,val2):
    Somme=val1+val2
    Soustaction=val1-val2
    division=val1/val2
    multiplication=val1*val2
    print(f" {val1} + {val2} = {Somme}")
    print(f" {val1} - {val2} = {Soustaction} ")
    print(f" {val1} / {val2} = {division} ")
    print(f" {val1} * {val2} = {multiplication} ")

Opérations(val1,val2)
